module.exports = {
  sum: function (a, b) {
    return a + b;
  },
  multiply: function (c,d) {
    return c * d;
  },
  square: function (e) {
    return e * e;
  },
  divide: function (f, g) {
    return f/g;
  },
  subtract: function (h,i){
    return h-i;
  },
  remainder: function (j,k){
    return j%k;
  }
};
